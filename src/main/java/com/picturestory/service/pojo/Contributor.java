public class Contributor {
    private int contributorUserId;
    private String contributorFbId;
    private String contributorUserEmail;
    private String contributorUserName;
    private String contributorUserImage;
    private String contributorUserDesc;
    private String contributorGcmId;
    private boolean contributorIsContributor;
    private String contributorMobileNumber;

    public int getContributorUserId() {
        return contributorUserId;
    }

    public void setContributorUserId(int contributorUserId) {
        this.contributorUserId = contributorUserId;
    }

    public String getContributorFbId() {
        return contributorFbId;
    }

    public void setContributorFbId(String contributorFbId) {
        this.contributorFbId = contributorFbId;
    }

    public String getContributorUserEmail() {
        return contributorUserEmail;
    }

    public void setContributorUserEmail(String contributorUserEmail) {
        this.contributorUserEmail = contributorUserEmail;
    }

    public String getContributorUserName() {
        return contributorUserName;
    }

    public void setContributorUserName(String contributorUserName) {
        this.contributorUserName = contributorUserName;
    }

    public String getContributorUserImage() {
        return contributorUserImage;
    }

    public void setContributorUserImage(String contributorUserImage) {
        this.contributorUserImage = contributorUserImage;
    }

    public String getContributorUserDesc() {
        return contributorUserDesc;
    }

    public void setContributorUserDesc(String contributorUserDesc) {
        this.contributorUserDesc = contributorUserDesc;
    }

    public String getContributorGcmId() {
        return contributorGcmId;
    }

    public void setContributorGcmId(String contributorGcmId) {
        this.contributorGcmId = contributorGcmId;
    }

    public boolean isContributorIsContributor() {
        return contributorIsContributor;
    }

    public void setContributorIsContributor(boolean contributorIsContributor) {
        this.contributorIsContributor = contributorIsContributor;
    }

    public String getContributorMobileNumber() {
        return contributorMobileNumber;
    }

    public void setContributorMobileNumber(String contributorMobileNumber) {
        this.contributorMobileNumber = contributorMobileNumber;
    }
}
